# This function take a three parameter and addition first two numbers and multiply them by the third number
# ---> And the function return the result 
def calculate(num1, num2, num3):
  return (num1 + num2) * num3

# Here call the function three times and print the result 
print(calculate(1,2,2)) # The result will be (6)

print(calculate(5,5,3)) # The result will be (30)

print(calculate(3,2,3)) # The result will be (15)


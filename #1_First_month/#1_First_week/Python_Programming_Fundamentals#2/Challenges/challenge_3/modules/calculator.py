# This Function for addition numbers
def addition(num1, num2):
  return num1 + num2

# This Function for subtraction numbers
def subtraction(num1, num2):
  return num1 - num2

# This Function for multiplication numbers
def multiplication(num1, num2):
  return num1 * num2

# This Function for division numbers
def division(num1, num2):
  return num1 / num2